</p>
<p>
<strong>E101 Prize Winners</strong><br>
To create a new prize winner, make a new page and choose "E101 Prize" from "Page Attributes" subpanel at the right. 
Add a portrait image, use a small-ish image, not to exceed 300 pixels. WP will create a medium sized image which is cropped so as not to exceed 300 pixels in either direction. 
Make the image left-aligned. Add text and a link to an uploaded pdf of their paper. Check the formatting of past winners and copy it.
